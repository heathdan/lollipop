public class SpinnerNotDisappearException extends GenericException {

    public SpinnerNotDisappearException() {
    }

    public SpinnerNotDisappearException(String message) {
        super(message);
    }

    public SpinnerNotDisappearException(String message, Throwable cause) {
        super(message, cause);
    }

    public SpinnerNotDisappearException(Throwable cause) {
        super(cause);
    }
}
